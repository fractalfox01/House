public class Window {
    private String shape;
    private int width;
    private int height;

    public Window(String shape, int width, int height) {
        this.shape = shape;
        this.width = width;
        this.height = height;
    }

    public String getShape() {
        return shape;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}
